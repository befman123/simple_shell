#include "shell.h"
/**
 *_memcpy - copy memory from one pointer to the other
 *@src: copy from
 *@dest: copy to
 *@len: length in gytes to be copied
 *
 *Return: the destination void pointer
 */
void *_memcpy(void *src, void *dest, size_t len)
{
	while (len--)
		*((char *)dest + len) = *((char *)src + len);
	return ((void *)dest);
}
